
"""
3. Un revendedor compra un artículo a un precio determinado. Obtener el precio en que lo debe
vender para obtener una ganancia del 25%
"""

# Declaracion de variables
precio_compra = 0
precio_venta = 0

# Entrada de datos
precio_compra = float(input("Ingrese el precio de compra del artículo: "))
# Proceso
precio_venta = precio_compra * 1.25
# Salida
print(f"El precio de venta para obtener una ganancia del 25% es: {precio_venta}")
